#================================================================================
class Message:
#================================================================================
# Represents a single message with only one content type: a single process ID.
#================================================================================
# Methods
    #----------------------------------------------------------------------------
    # - Message Constructor
    #----------------------------------------------------------------------------
    # * content : the message's content
    #----------------------------------------------------------------------------
    def __init__(self, content):
        self.content = content

    #----------------------------------------------------------------------------
    # - Represent as String (Overload)
    #----------------------------------------------------------------------------
    def __str__(self):
        return "M<" + str(self.content) + ">"
# Members
    content = 0
